package com.welab.wefe.gateway.common;

/**
 * Metadata persistence constant
 *
 * @author aaron.li
 **/
public enum TransferMetaCachePersistentTypeEnum {
    /**
     * Local file system
     */
    LOCAL_FILE_SYS("localFileSys", "Local file system");

    /**
     * type
     */
    private String type;
    /**
     * Name Description
     */
    private String name;

    TransferMetaCachePersistentTypeEnum(String type, String name) {
        this.type = type;
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
