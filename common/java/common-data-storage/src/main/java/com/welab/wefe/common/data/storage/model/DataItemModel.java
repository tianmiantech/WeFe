package com.welab.wefe.common.data.storage.model;

/**
 * @author yuxin.zhang
 */
public class DataItemModel<K, V> extends AbstractModel {
    private K k;
    private V v;

    public DataItemModel() {
    }


    public DataItemModel(K k, V v) {
        this.k = k;
        this.v = v;
    }

    public K getK() {
        return k;
    }

    public void setK(K k) {
        this.k = k;
    }

    public V getV() {
        return v;
    }

    public void setV(V v) {
        this.v = v;
    }
}
