package com.welab.wefe.common.data.mongodb.entity.contract.tool;

import com.alibaba.fastjson.JSONObject;
import com.welab.wefe.common.data.mongodb.entity.AbstractNormalMongoModel;

import java.math.BigInteger;

/**
 * @author aaron.li
 * @date 2022/1/5 15:52
 **/
public class TransactionResponseDetailInfo extends AbstractNormalMongoModel {
    private BigInteger blockNumber;
    private String transactionHash;
    private String contractName;
    private String contractAddress;
    private String eventName;
    private JSONObject data;

    public BigInteger getBlockNumber() {
        return blockNumber;
    }

    public void setBlockNumber(BigInteger blockNumber) {
        this.blockNumber = blockNumber;
    }

    public String getContractName() {
        return contractName;
    }

    public void setContractName(String contractName) {
        this.contractName = contractName;
    }

    public String getContractAddress() {
        return contractAddress;
    }

    public void setContractAddress(String contractAddress) {
        this.contractAddress = contractAddress;
    }

    public String getEventName() {
        return eventName;
    }

    public void setEventName(String eventName) {
        this.eventName = eventName;
    }

    public JSONObject getData() {
        return data;
    }

    public void setData(JSONObject data) {
        this.data = data;
    }

    public String getTransactionHash() {
        return transactionHash;
    }

    public void setTransactionHash(String transactionHash) {
        this.transactionHash = transactionHash;
    }
}
