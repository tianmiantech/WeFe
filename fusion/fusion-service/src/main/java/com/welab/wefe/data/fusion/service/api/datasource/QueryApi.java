package com.welab.wefe.data.fusion.service.api.datasource;

import com.welab.wefe.common.exception.StatusCodeWithException;
import com.welab.wefe.common.fieldvalidate.annotation.Check;
import com.welab.wefe.common.web.api.base.AbstractApi;
import com.welab.wefe.common.web.api.base.Api;
import com.welab.wefe.common.web.dto.ApiResult;
import com.welab.wefe.common.wefe.enums.DatabaseType;
import com.welab.wefe.data.fusion.service.dto.base.PagingInput;
import com.welab.wefe.data.fusion.service.dto.base.PagingOutput;
import com.welab.wefe.data.fusion.service.dto.entity.AbstractOutputModel;
import com.welab.wefe.data.fusion.service.service.DataSourceService;
import org.springframework.beans.factory.annotation.Autowired;

/**
 * @author Jacky.jiang
 */
@Api(path = "data_source/query", name = "查询数据源")
public class QueryApi extends AbstractApi<QueryApi.Input, PagingOutput<QueryApi.Output>> {

    @Autowired
    DataSourceService dataSourceService;

    @Override
    protected ApiResult<PagingOutput<Output>> handle(Input input) throws StatusCodeWithException {
        return success(dataSourceService.query(input));
    }

    public static class Input extends PagingInput {
        @Check(name = "数据源名称")
        private String name;

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }
    }

    public static class Output extends AbstractOutputModel {

        private String name;

        private DatabaseType databaseType;

        private String host;

        private Integer port;

        private String databaseName;

        private String userName;

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public DatabaseType getDatabaseType() {
            return databaseType;
        }

        public void setDatabaseType(DatabaseType databaseType) {
            this.databaseType = databaseType;
        }

        public String getHost() {
            return host;
        }

        public void setHost(String host) {
            this.host = host;
        }

        public Integer getPort() {
            return port;
        }

        public void setPort(Integer port) {
            this.port = port;
        }

        public String getDatabaseName() {
            return databaseName;
        }

        public void setDatabaseName(String databaseName) {
            this.databaseName = databaseName;
        }

        public String getUserName() {
            return userName;
        }

        public void setUserName(String userName) {
            this.userName = userName;
        }
    }
}
