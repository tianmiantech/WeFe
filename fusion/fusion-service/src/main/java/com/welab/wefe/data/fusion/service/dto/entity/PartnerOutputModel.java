package com.welab.wefe.data.fusion.service.dto.entity;

/**
 * @author hunter.zhao
 */
public class PartnerOutputModel extends AbstractOutputModel{
    String partnerId;

    String name;

    String rsaPublicKey;

    String baseUrl;

    int openSocketPort;

    public String getPartnerId() {
        return partnerId;
    }

    public void setPartnerId(String partnerId) {
        this.partnerId = partnerId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRsaPublicKey() {
        return rsaPublicKey;
    }

    public void setRsaPublicKey(String rsaPublicKey) {
        this.rsaPublicKey = rsaPublicKey;
    }

    public String getBaseUrl() {
        return baseUrl;
    }

    public void setBaseUrl(String baseUrl) {
        this.baseUrl = baseUrl;
    }

    public int getOpenSocketPort() {
        return openSocketPort;
    }

    public void setOpenSocketPort(int openSocketPort) {
        this.openSocketPort = openSocketPort;
    }
}
