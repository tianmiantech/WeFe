<template>
    <el-card
        v-loading="loading"
        class="page"
        shadow="never"
    >
        <h2 class="title">编辑客户</h2>

        <el-form
            ref="client"
            :model="client"
            label-width="90px"
            :rules="rules"
        >
            <el-form-item
                label="客户名称"
                prop="name"
            >
                <el-input
                    v-model="client.name"
                    :maxlength="32"
                    :minlength="4"
                    show-word-limit
                />
            </el-form-item>
            <el-form-item
                label="客户邮箱"
                prop="email"
            >
                <el-input v-model="client.email" />
            </el-form-item>
            <el-form-item
                label="IP 白名单"
                prop="ipAdd"
            >
                <el-input
                    v-model="client.ipAdd"
                    placeholder="支持多个，英文逗号分隔"
                />
            </el-form-item>
            <el-form-item
                label="客户 code"
                prop="code"
            >
                <el-input
                    v-model="client.code"
                    :disabled="clientId !== ''"
                    placeholder="建议格式：[公司简称]-[手机号]-[日期]"
                    :maxlength="60"
                    :minlength="4"
                    show-word-limit
                />
            </el-form-item>
            <el-form-item
                label="公钥"
                prop="pubKey"
            >
                <el-input
                    v-model="client.pubKey"
                    type="textarea"
                />
            </el-form-item>

            <el-form-item
                label="状态："
                prop="status"
            >
                <el-radio
                    v-model="client.status"
                    :label="1"
                >
                    正常
                </el-radio>
                <el-radio
                    v-model="client.status"
                    :label="0"
                >
                    禁用
                </el-radio>
            </el-form-item>

            <el-form-item label="备注">
                <el-input
                    v-model="client.remark"
                    type="textarea"
                    rows="5"
                    :maxlength="300"
                    :minlength="0"
                    show-word-limit
                />
            </el-form-item>
            <el-form-item>
                <el-button
                    type="primary"
                    @click="onSubmit"
                >
                    提交
                </el-button>
                <router-link
                    :to="{
                        name: 'client-list',
                    }"
                >
                    <el-button>返回</el-button>
                </router-link>
            </el-form-item>
        </el-form>
    </el-card>
</template>

<script>
import { mapGetters } from 'vuex';

export default {
    name: 'ClientEdit',
    data() {

        const util = {
            isValidIp (e) {
                // 去除后面多余的 “,”
                const reg = /,+$/gi;
                const ip = e.replace(reg, '');

                return /^(?:(?:^|,)(?:[0-9]|[1-9]\d|1\d{2}|2[0-4]\d|25[0-5])(?:\.(?:[0-9]|[1-9]\d|1\d{2}|2[0-4]\d|25[0-5])){3})+$/.test(ip);
            },
        };

        const validateIpAdd = (rule, value, callback) => {
            if (!this.client.ipAdd) {
                return callback(new Error('IP 地址不能为空'));
            } else if (!util.isValidIp(this.client.ipAdd)) {
                return callback(new Error('请输入合法的IP'));
            } else {
                callback();
            }
        };

        const validateStatus = (rule, value, callback) => {
            if (this.client.status === '') {
                return callback(new Error('请选择客户状态'));
            } else {
                callback();
            }
        };

        return {
            loading: false,
            client:  {
                id:     '',
                name:   '',
                pubKey: '',
                email:  '',
                ipAdd:  '',
                remark: '',
                code:   '',
                status: '',
            },
            rules: {

                name: [
                    { required: true, message: '请输入客户名称', trigger: 'blur' },
                ],
                email: [
                    { required: true, message: '请输入邮箱', trigger: 'change' },
                ],
                ipAdd: [
                    // {required: true, message: '请输入IP白名单', trigger: 'change'}
                    { required: true, validator: validateIpAdd, trigger: 'blur' },
                ],
                status: [
                    { required: true, validator: validateStatus, trigger: 'change' },
                ],
                code: [
                    { required: true, message: '请输入客户code', trigger: 'change' },
                ],
                pubKey: [
                    { required: true, message: '请输入公钥', trigger: 'change' },
                ],
            },
            clientId: '',
        };
    },

    computed: {
        ...mapGetters(['userInfo']),
    },
    async created() {

        this.loading = true;
        if (this.$route.query.id) {
            this.clientId = this.$route.query.id;
            await this.getClientById(this.$route.query.id);
        }
        this.client.status = this.$route.query.status;
        this.loading = false;
    },
    methods: {


        onSubmit() {
            this.$refs.client.validate(async (valid) => {
                if (valid) {
                    const { code } = await this.$http.post({
                        url:  '/client/update',
                        data: {
                            id:        this.client.id,
                            name:      this.client.name,
                            email:     this.client.email,
                            ipAdd:     this.client.ipAdd,
                            pubKey:    this.client.pubKey,
                            remark:    this.client.remark,
                            updatedBy: this.userInfo.nickname,
                            status:    this.client.status,
                        },
                    });

                    if (code === 0) {
                        setTimeout(() => {
                            this.$message('提交成功!');
                        }, 1000);
                        this.$router.push({
                            name: 'client-list',
                        });
                    }
                }
            });

        },


        async getClientById(id) {
            const { code, data } = await this.$http.post({
                url:  '/client/query-one',
                data: {
                    id,
                },

            });

            if (code === 0) {
                this.client.id = data.id;
                this.client.name = data.name;
                this.client.email = data.email;
                this.client.ipAdd = data.ip_add;
                this.client.pubKey = data.pub_key;
                this.client.remark = data.remark;
                this.client.code = data.code;
                this.client.status = data.status;
            }
        },
    },


};
</script>

