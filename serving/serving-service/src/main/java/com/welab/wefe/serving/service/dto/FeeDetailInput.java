package com.welab.wefe.serving.service.dto;

import java.math.BigDecimal;
import java.util.UUID;

/**
 * @author ivenn.zheng
 * @date 2021/12/24
 */
public class FeeDetailInput {

    private String id = UUID.randomUUID().toString().replaceAll("-", "");

    private String serviceId;

    private String clientId;

    private Long totalRequestTimes;

    private BigDecimal totalFee;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getServiceId() {
        return serviceId;
    }

    public void setServiceId(String serviceId) {
        this.serviceId = serviceId;
    }

    public String getClientId() {
        return clientId;
    }

    public void setClientId(String clientId) {
        this.clientId = clientId;
    }

    public Long getTotalRequestTimes() {
        return totalRequestTimes;
    }

    public void setTotalRequestTimes(Long totalRequestTimes) {
        this.totalRequestTimes = totalRequestTimes;
    }

    public BigDecimal getTotalFee() {
        return totalFee;
    }

    public void setTotalFee(BigDecimal totalFee) {
        this.totalFee = totalFee;
    }
}
